// Um passeio pode aparecer em mais de um filtro: `categoria` é a principal
// e `categorias` (opcional) lista filtros extras em que ele também aparece.
export function tourNaCategoria(tour, cat) {
  if (cat === 'todos') return true
  return tour.categoria === cat || (tour.categorias?.includes(cat) ?? false)
}

